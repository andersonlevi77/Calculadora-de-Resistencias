    private void JbandasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbandasActionPerformed
        Interfaz n= new Interfaz();                                     //se crea un objeto para cada interfaz  para usarlo en las opciones del combobox
         Interfaz_2 k = new Interfaz_2();                                //se crea un objeto para cada interfaz 
        if(Jbandas.getSelectedIndex()==1){                               //si la selección del usuario es en la posición 1 del combobox, teniendo en cuenta que la posicion 0 no está utilizandose
            n.setVisible(true);                                          //se muestra en pantalla el formulario de 4 bandas       
            dispose();                                                      //método para que se cierre el formulario que estaba abierto anteriormente a la elección
            
        }
        else if(Jbandas.getSelectedIndex()==2){                          //de lo contrario si se selecciona la segunda opción
            k.setVisible(true);                                             //aparecerá el formulario de las 5 bandas
            dispose();                                                   //método para que se cierre el formulario que estaba abierto anteriormente a la elección   
            
        }        
    }//GEN-LAST:event_JbandasActionPerformed

