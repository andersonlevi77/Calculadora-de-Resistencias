    class FondoPanel extends JPanel                 //clase interna llamada fondo panel extendida de la Clase JPanel
    {
        private Image imagen;                       //variable privada de tipo Image llamada imagen
        
        @Override                                   //para anular un método de una superclase, Se utiliza para asegurar que un método de superclase esté anulado y no simplemente sobrecargado.
        public void paint(Graphics g)               //Método paint que tiene como parámetro Graphics g
        {
            imagen = new ImageIcon(getClass().getResource("/img/fondoprueba2.jpg")).getImage(); //se crea un objeto y se utiliza el método getResource para localizar la imagen
            
            g.drawImage(imagen,0, 0, getWidth(), getHeight(),this);    //para dibujar la imagen y también cambiar su tamaño. getWidth para tener el ancho del panel y getHeight para tener la altura del panel.
            
            setOpaque(false);          //Establece la transparencia del fondo de un texto estampado
            
            super.paint(g);
        }
    }
    
